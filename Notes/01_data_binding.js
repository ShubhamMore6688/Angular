//two types of databinding 
// 1. one way
// 2.two way

// one way - in this we can share data either from component to view or view to component at a time
// for one way databinding there are 3 ways
// - string interpolation {{data}}
// - property binding [html attribute]
// - event binding (from view to component) (eg. like button)
// two way - in this data shared from view to component and again it is shared from component to view at the same time
